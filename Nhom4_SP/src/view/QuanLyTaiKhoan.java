    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String tenTK = txtTaiKhoan.getText();
        String matKhau = txtMatKhau.getText();
        String role = txtRole.getText();
        for (TaiKhoan taiKhoan : lst) {
            if (tenTK.equals(taiKhoan.getTenTK())) {
                JOptionPane.showMessageDialog(rootPane, "Tên tài khoản đã tồn tại");
                return;
            }
        }
        if (matKhau.length() < 6) {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu phải chứa ít nhất 6 ký tự");
        } else {
            TaiKhoan tk = new TaiKhoan(tenTK, matKhau, role, "Enable");
            KhachHang kh = new KhachHang(tk.getMaKh());
            KhachHangController.insert(kh);
            TaiKhoanController.insert(tk);
            updateTable();
        }
    }//GEN-LAST:event_btnThemActionPerformed

