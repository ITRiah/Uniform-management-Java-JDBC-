    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int dongChon = dgvTaiKhoan.getSelectedRow();

        if (dongChon > -1) {
            dongChon += currentPage * recordsPerPage - recordsPerPage;
            String tenTK = dgvTaiKhoan.getValueAt(dongChon, 0).toString();
            if (!tenTK.equals(txtTaiKhoan.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Không được sửa Tên tài khoản");
            }
        }

        String tenTK = txtTaiKhoan.getText();
        String matKhau = txtMatKhau.getText();
        String role = txtRole.getText();

        if (matKhau.length() < 6)
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu phải chứa ít nhất 6 ký tự");
        else {
            TaiKhoanController.update(matKhau, role, tenTK);
            updateTable();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

