    private void btnDisableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisableActionPerformed
        // TODO add your handling code here:
        int dong = dgvTaiKhoan.getSelectedRow();
        if (dong != -1) {
            dong += currentPage * recordsPerPage - recordsPerPage;

            TaiKhoan tk = lst.get(dong);
            if (tk.getTinhTrang().equals("Disable")) {
                JOptionPane.showMessageDialog(rootPane, "Tài khoản đang ở trạng thái Disable");
            } else {
                TaiKhoanController.updateStatus("Disable", tk.getTenTK());
                JOptionPane.showMessageDialog(rootPane, "Cập nhật trạng thái thành công");
                updateTable();
            }
        }
    }//GEN-LAST:event_btnDisableActionPerformed

